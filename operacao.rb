# Missão 3
# Crie um programa que que receba dois números inteiros
# e no final exiba a soma, subtração, adição e divisão entre eles.

#Entrada de Dados
print "\nDigite o primeiro número inteiro: "
n1 = gets.chomp.to_i
print "Digite o segundo número inteiro: "
n2 = gets.chomp.to_i
#Saída de dados
puts "A soma deles é #{n1+n2}"
puts "A subtração deles é #{n1-n2}"
puts "A multiplicação deles é #{n1*n2}"
puts "A divisão deles é #{n1/n2}\n\n"

